// typeConversion.js - explicit and implicit conversions
console.log("'5' + 2 ->", '5' + 2);       // string concatenation
console.log("'5' - 2 ->", '5' - 2);       // numeric subtraction -> 3
console.log("Number('123') ->", Number('123'));
console.log("parseInt('12px') ->", parseInt('12px'));
console.log("String(45) ->", String(45));
console.log("Boolean('') ->", Boolean(''));
console.log("!!'hello' ->", !!'hello');   // double NOT to get boolean
